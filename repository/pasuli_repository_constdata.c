unsigned short number_of_implementation_state_entries = sizeof(implementationStates) / (2 * sizeof(char));

unsigned char getImplementationState(unsigned int id)
{
	if ((id <= 0) || (id > NUMB_OF_SURFACES))
	{
		return 0;
	}

	for (int i = 0; i < number_of_implementation_state_entries; i++)
	{
		if (implementationStates[i][0] == id)
		{
			return implementationStates[i][1];
		}
	}

	return 0;
}

