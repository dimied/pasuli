unsigned short id_and_constants_entries = sizeof(id_and_constants) / (3 * sizeof(char));

int findDefaultConstants(unsigned int id, double *pConstants, int size)
{
	if (id >= 255)
	{
		// Not found
		return -1;
	}

	for (unsigned short idx = 0; idx < id_and_constants_entries; idx++)
	{
		int idInArray = id_and_constants[idx][0];
		if (id == idInArray)
		{
			int length = id_and_constants[idx][1];
			if (length > 0)
			{
				if (length < size)
				{
					// Not enough space to store
					return -2;
				}
				int typeOrValue = id_and_constants[idx][2];

				if (typeOrValue == ALL_ONES || typeOrValue == ALL_HALFES)
				{
					double v = typeOrValue == ALL_ONES ? 1.0 : 0.5;

					for (int j = 0; j < length; j++)
					{
						pConstants[j] = v;
					}
				}

				if (typeOrValue >= STD1_VALUES && typeOrValue <= TORUS1_VALUES)
				{
					int idx = (typeOrValue - STD1_VALUES) * 6;

					if (idx > sizeof(constants_multiplied) / sizeof(char) ||
						length > 6)
					{
						// INTERNAL error
						return -3;
					}

					for (int j = 0; j < length; j++)
					{
						pConstants[j] = (constants_multiplied[j] * 1.0) / 10.0;
					}
				}
				else
				{
					for (int j = 0; j < length; j++)
					{
						pConstants[j] = typeOrValue * 1.0;
					}
				}
			}
			return length;
		}
	}

	// Not found
	return -1;
}

