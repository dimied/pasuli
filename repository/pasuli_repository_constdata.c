unsigned short number_of_entries_in_pasuli_surface_desc_array = sizeof(parsur_desc_array) / sizeof(PaSuLiTextDescType);

char *findDescriptionTextByID(unsigned char ID)
{
	if ((ID <= 0) || (ID > 255))
	{
		return 0;
	}

	for (unsigned short idx = 0; idx < number_of_entries_in_pasuli_surface_desc_array; idx++)
	{
		if (parsur_desc_array[idx].ID == ID)
		{
			char **ptr = parsur_desc_array[idx].pszDesc;

			if (ptr != 0)
			{
				return *ptr;
			}
		}
	}
	return 0;
}
